#include "MESCHACH/INCLUDES/meminfo.h"

#include "MESCHACH_EF/INCLUDES/finite_elements.h"

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int mem_attach_list2(void)
{
   return mem_attach_list(MY_LIST2, NB_STRUCTS2, my_names2, my_frees2, my_tnums2);
}

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int err_attach_list2(void)
{
   return err_list_attach(MY_LIST2, NB_ERRS2, my_err_mesg2);
}

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int warn_attach_list2(void)
{
   return warn_list_attach(MY_LIST2, NB_ERRS2, my_warn_mesg2);
}

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int err_warn_attach_lists2(void)
{
   err_attach_list2();
   warn_attach_list2();

   return 1;
}

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int mem_stat_reg2(void **var, int type)
{
   return mem_stat_reg_list(var,type, MY_LIST2);
}

int mem_stat_free2(int mark)
{
   return mem_stat_free_list(mark, MY_LIST2);
}

