#include "MESCHACH/INCLUDES/meminfo.h"

#include "MESCHACH_EF/INCLUDES/boundary_conditions.h"

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int mem_attach_list4(void)
{
   return mem_attach_list(MY_LIST4, NB_STRUCTS4, my_names4, my_frees4, my_tnums4);
}

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int err_attach_list4(void)
{
   return err_list_attach (MY_LIST4, NB_ERRS4 , my_err_mesg4);
}

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int warn_attach_list4(void)
{
   return warn_list_attach(MY_LIST4, NB_WARNS4, my_warn_mesg4);
}

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int err_warn_attach_lists4(void)
{
   err_attach_list4();
   warn_attach_list4();

   return 1;
}

/*-----------------------------------------------------------------------------------------------------------*/
/*-----------------------------------------------------------------------------------------------------------*/

int mem_stat_reg4(void **var, int type)
{
   return mem_stat_reg_list(var,type, MY_LIST4);
}

int mem_stat_free4(int mark)
{
   return mem_stat_free_list(mark, MY_LIST4);
}

